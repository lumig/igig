#ifndef LingDou_LDProperty_h
#define LingDou_LDProperty_h

/*
 *	@brief
 *	@param CLASSNAME	类名
 *	@param PROPERTYNAME	属性名
 *	@return
 */
#define MAKE_CLASS_PROPERTY(CLASSNAME,PROPERTYNAME) @property (nonatomic,strong)CLASSNAME *PROPERTYNAME;

#define MAKE_NSSTRING_PROPERTY(STRINGNAME) MAKE_CLASS_PROPERTY(NSString,STRINGNAME)

#define MAKE_NSNUMBER_PROPERTY(NSNUMBERNAME) MAKE_CLASS_PROPERTY(NSNumber,NSNUMBERNAME)

#define MAKE_NSARRAY_PROPERTY(NSARRAYNAME) MAKE_CLASS_PROPERTY(NSArray,NSARRAYNAME)

#define MAKE_NSDICTIONARY_PROPERTY(NSDICTIONARYNAME) MAKE_CLASS_PROPERTY(NSDictionary,NSDICTIONARYNAME)

#endif
