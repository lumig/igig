#import "UIFont+Kit.h"

@implementation UIFont (Kit)
+ (UIFont*)chineseFontWithSize:(CGFloat)size
{
    return [UIFont fontWithName:@"STHeitiSC-Light" size:size];
}

+ (UIFont*)boldChineseFontWithSize:(CGFloat)size
{
     return [UIFont fontWithName:@"STHeitiSC-Medium" size:size];
}
@end
